import mongoose from "mongoose";

const Schema = mongoose.Schema;

const blogpostSchema = new Schema(
  {
    title: {
      type: String,
      required: true,
    },
    author: {
      type: String,
      required: true,
    },
    body: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

export default mongoose.model("Blogpost", blogpostSchema);
